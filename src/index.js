export default function getFlagEmoji(locale) {
  const split = locale.toUpperCase().split(/[-_]/);
  let code = split.pop();

  if (!/^[A-Z]{2}$/.test(code)) {
    try {
      code = new Intl.Locale(locale).maximize().region.toUpperCase();
    } catch (e) {
      code = '';
    }
  }

  return flagMap[code] || '';
}